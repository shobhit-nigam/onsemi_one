# operations
fruits = {'apples', 'oranges', 'straberries', 'apples', 'bananas', 'oranges', 'watermelons'}
basket = {'mangoes', 'avocados', 'apples', 'bananas', 'tomatoes', 'plums'}

print("fruits =", fruits)
print("basket =", basket)

print("union =", fruits | basket)
print("intersection =", fruits & basket)
print("sym difference =", fruits ^ basket)
print("difference =", fruits - basket)
