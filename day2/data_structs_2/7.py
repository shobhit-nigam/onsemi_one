# slicing - step
num_a = [8, 9, 3, 4, 6, 10, 20, 33, 11]

print(num_a)
print(num_a[2:7])   # 3, 4, 6, 10, 20
print(num_a[:5])    #   num_a[0:5]
print(num_a[2:])    #   num_a[2:9]  defaults to end
print(num_a[:])     #  start to end
print(num_a[0:7:3]) # step of 3,
                    # evry 3rd member
print(num_a[::2])


