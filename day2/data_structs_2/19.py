# dictionary
# indexed to any level

fruits = {'general':'apples', 'citrus':('oranges', 'limes'), 'berry':['straberries', 'kiwifruit'], 'melons':'watermelons'}

print("fruits =", fruits)
print("----")

print("fruits['citrus'] =", fruits['citrus'])
print("----")

print("fruits['citrus'][1] =", fruits['citrus'][1])
print("----")

print("fruits['citrus'][1][2] =", fruits['citrus'][1][2])
print("----")
